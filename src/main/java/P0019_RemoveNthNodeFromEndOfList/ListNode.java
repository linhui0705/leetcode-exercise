package P0019_RemoveNthNodeFromEndOfList;

/**
 * Title: ListNode
 * Description:
 *
 * @author Lin Hui
 * Created on 2020/2/21 22:34
 */
public class ListNode {
    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}
